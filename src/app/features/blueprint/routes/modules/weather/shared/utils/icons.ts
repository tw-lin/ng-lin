/**
 * 根據適宜度等級取得圖示
 */
export function getSuitabilityIcon(level: 'excellent' | 'good' | 'fair' | 'poor' | 'dangerous'): string {
  const iconMap = {
    excellent: '✅', // 優秀
    good: '👍', // 良好
    fair: '⚠️', // 尚可
    poor: '❌', // 不佳
    dangerous: '🚫' // 危險
  };
  return iconMap[level];
}
